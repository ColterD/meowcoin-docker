#!/bin/bash

# Meowcoin Node Helper Functions
# This script contains common functions used by other scripts

# Global variables
MEOWCOIN_DATA=${MEOWCOIN_DATA:-/data}
MEOWCOIN_CONFIG=${MEOWCOIN_CONFIG:-/config}
MEOWCOIN_HOME=${HOME:-/home/meowcoin}
MEOWCOIN_CLI="/usr/local/bin/meowcoin-cli -conf=${MEOWCOIN_CONFIG}/meowcoin.conf"

# Logging functions
log_info() {
    echo -e "\e[32m[INFO]\e[0m $1"
}

log_warn() {
    echo -e "\e[33m[WARN]\e[0m $1"
}

log_error() {
    echo -e "\e[31m[ERROR]\e[0m $1" >&2
}

# Safely execute a command and return status
safe_exec() {
    local output
    local status=0
    
    output=$(eval "$@" 2>&1) || status=$?
    
    if [ $status -ne 0 ]; then
        log_error "Command failed: $1"
        log_error "Output: $output"
    fi
    
    return $status
}

# Get meowcoin version
get_version() {
    if [ -f "/usr/local/bin/meowcoind" ]; then
        meowcoind -version | head -n 1 | cut -d ' ' -f 1-2 || echo "Unknown"
    else
        echo "Unknown"
    fi
}

# Display ASCII banner
display_banner() {
    local text="$1"
    local width=$(( ${#text} + 4 ))
    
    echo ""
    printf "%0.s=" $(seq 1 $width)
    echo ""
    echo "  $text"
    printf "%0.s=" $(seq 1 $width)
    echo ""
}

